# -----------------------------------------------------------------------------
# Factory
# -----------------------------------------------------------------------------
_BACKENDS = {
    "duckdb": DuckDBEngine,
    "snowflake": SnowflakeEngine,
    "databricks": DatabricksEngine,
}


def get_engine(backend: str = "duckdb", **kwargs) -> DBEngine:
    """Return a configured ``DBEngine`` for the requested backend.

    Parameters
    ----------
    backend:
        One of ``"duckdb"`` (default), ``"snowflake"``, ``"databricks"``.
    **kwargs:
        Passed through to the engine constructor. For DuckDB, the most
        useful is ``db_path``.

    Examples
    --------
    >>> db = get_engine()                                # default DuckDB
    >>> db = get_engine("duckdb", db_path="/tmp/x.db")   # custom path
    """
    backend = backend.lower()
    if backend not in _BACKENDS:
        raise ValueError(
            f"Unknown backend: {backend!r}. "
            f"Supported: {sorted(_BACKENDS)}"
        )
    return _BACKENDS[backend](**kwargs)


